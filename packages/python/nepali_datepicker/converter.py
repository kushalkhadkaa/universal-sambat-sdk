@dataclass(frozen=True)
class BsDate:
    year: int
    month: int
    day: int

    def __str__(self) -> str:
        return f"{self.year:04d}-{self.month:02d}-{self.day:02d}"

    @property
    def iso(self) -> str:
        return str(self)

    def to_ad(self) -> "AdDate":
        return bs_to_ad(self.year, self.month, self.day)


@dataclass(frozen=True)
class AdDate:
    year: int
    month: int
    day: int

    def __str__(self) -> str:
        return f"{self.year:04d}-{self.month:02d}-{self.day:02d}"

    @property
    def iso(self) -> str:
        return str(self)

    @property
    def date(self) -> datetime.date:
        return datetime.date(self.year, self.month, self.day)

    def to_bs(self) -> BsDate:
        return ad_to_bs(self.date)


def _days_in_month(year: int, month: int) -> int:
    row = _DATA.get(year)
    if not row:
        raise ValueError(f"BS year {year} outside supported range {_MIN_YEAR}–{_MAX_YEAR}")
    return row[month - 1]


def _days_from_base(year: int, month: int, day: int) -> int:
    total = 0
    for y in range(_MIN_YEAR, year):
        row = _DATA.get(y)
        if row:
            total += sum(row)
    for m in range(1, month):
        total += _days_in_month(year, m)
    return total + day - 1


def bs_to_ad(year: int, month: int, day: int) -> AdDate:
    """Convert Bikram Sambat date to Gregorian AD date."""
    if month < 1 or month > 12:
        raise ValueError(f"Month {month} out of range 1–12")
    dim = _days_in_month(year, month)
    if day < 1 or day > dim:
        raise ValueError(f"Day {day} invalid for BS {year}-{month} (max {dim})")
    ad = _BASE_AD + datetime.timedelta(days=_days_from_base(year, month, day))
    return AdDate(ad.year, ad.month, ad.day)


def ad_to_bs(date: datetime.date | str) -> BsDate:
    """Convert Gregorian date to Bikram Sambat date."""
    if isinstance(date, str):
        date = datetime.date.fromisoformat(date)
    delta = (date - _BASE_AD).days
    if delta < 0:
        raise ValueError("Date is before the supported BS range (1970 BS).")
    for y in range(_MIN_YEAR, _MAX_YEAR + 1):
        row = _DATA.get(y)
        if not row:
            continue
        for m, days in enumerate(row, start=1):
            if delta < days:
                return BsDate(y, m, delta + 1)
            delta -= days
    raise ValueError("Date is after the supported BS range (2100 BS).")


def bs2ad(bs_str: str) -> str:
    """'2081-01-15' → '2024-04-28'"""
    y, m, d = map(int, bs_str.split("-"))
    return str(bs_to_ad(y, m, d))


def ad2bs(ad_str: str) -> str:
    """'2024-04-28' → '2081-01-15'"""
    return str(ad_to_bs(ad_str))


def today_bs() -> BsDate:
    """Current date in Bikram Sambat."""
    return ad_to_bs(datetime.date.today())


def is_valid_bs(year: int, month: int, day: int) -> bool:
    try:
        bs_to_ad(year, month, day)
        return True
    except (ValueError, KeyError):
        return False


def days_diff(bs1: str | BsDate, bs2: str | BsDate) -> int:
    """Number of days between two BS dates (bs2 - bs1)."""
    d1 = ad_to_bs(bs1) if isinstance(bs1, str) else bs1
    d2 = ad_to_bs(bs2) if isinstance(bs2, str) else bs2
    return (bs_to_ad(d2.year, d2.month, d2.day).date - bs_to_ad(d1.year, d1.month, d1.day).date).days


def month_name(month: int, lang: str = "ne") -> str:
    if not 1 <= month <= 12:
        raise ValueError(f"Month {month} out of range 1–12")
    return (_MONTHS_NE if lang == "ne" else _MONTHS_EN)[month - 1]

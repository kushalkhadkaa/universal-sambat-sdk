const MONTHS_NE = ['बैशाख','जेठ','असार','साउन','भदौ','असोज','कार्तिक','मंसिर','पुस','माघ','फागुन','चैत'];
const MONTHS_EN = ['Baisakh','Jestha','Ashadh','Shrawan','Bhadra','Ashwin','Kartik','Mangsir','Poush','Magh','Falgun','Chaitra'];
const NP_DIGITS = ['०','१','२','३','४','५','६','७','८','९'];

function pad(n) { return String(n).padStart(2, '0'); }
function toNp(n) { return String(n).replace(/[0-9]/g, d => NP_DIGITS[+d]); }
function daysInMonth(y, m) { return (CALENDAR[y] || [])[m - 1] || null; }

function daysFromBase(y, m, d) {
  let total = 0;
  for (let yr = 1970; yr < y; yr++) {
    total += CALENDAR[yr].reduce((s, n) => s + n, 0);
  }
  for (let mo = 1; mo < m; mo++) total += daysInMonth(y, mo);
  return total + d - 1;
}

function bsToAd(year, month, day) {
  year = +year; month = +month; day = +day;
  const dim = daysInMonth(year, month);
  if (!dim) throw new RangeError(`BS ${year}-${month} outside supported range (1970–2100).`);
  if (day < 1 || day > dim) throw new RangeError(`Day ${day} invalid for BS ${year}-${month} (max ${dim}).`);
  const utc  = BASE_AD + daysFromBase(year, month, day) * MS_PER_DAY;
  const date = new Date(utc);
  const y = date.getUTCFullYear(), mo = date.getUTCMonth() + 1, dy = date.getUTCDate();
  return { year: y, month: mo, day: dy, iso: `${y}-${pad(mo)}-${pad(dy)}`, date };
}

function adToBs(adDate) {
  const d    = adDate instanceof Date ? adDate : new Date(adDate);
  if (isNaN(d)) throw new RangeError('Invalid AD date.');
  const utc  = Date.UTC(d.getFullYear(), d.getMonth(), d.getDate());
  let rem    = Math.round((utc - BASE_AD) / MS_PER_DAY);
  if (rem < 0) throw new RangeError('AD date before supported BS range.');
  for (let y = 1970; y <= 2100; y++) {
    for (let m = 1; m <= 12; m++) {
      const dim = daysInMonth(y, m);
      if (rem < dim) {
        const day = rem + 1;
        return { year: y, month: m, day, iso: `${y}-${pad(m)}-${pad(day)}`, weekday: d.getDay() };
      }
      rem -= dim;
    }
  }
  throw new RangeError('AD date after supported BS range (2100).');
}

function BS2AD(str) {
  const [y, m, d] = str.split('-').map(Number);
  return bsToAd(y, m, d).iso;
}

function AD2BS(str) {
  return adToBs(new Date(str)).iso;
}

function isValidBs(year, month, day) {
  try { bsToAd(+year, +month, +day); return true; } catch { return false; }
}

function todayBs() { return adToBs(new Date()); }

function daysDiff(bs1, bs2) {
  const a = bsToAd(bs1.year, bs1.month, bs1.day);
  const b = bsToAd(bs2.year, bs2.month, bs2.day);
  return Math.round((b.date - a.date) / MS_PER_DAY);
}

function formatBs(year, month, day, fmt, lang) {
  fmt  = fmt  || 'YYYY-MM-DD';
  lang = lang || 'ne';
  const use = lang === 'en';
  const mName = use ? MONTHS_EN[month - 1] : MONTHS_NE[month - 1];
  const yStr = use ? String(year) : toNp(year);
  const mStr = pad(month);
  const dStr = pad(day);
  switch (fmt) {
    case 'YYYY-MM-DD': return `${year}-${mStr}-${dStr}`;
    case 'DD/MM/YYYY': return `${dStr}/${mStr}/${year}`;
    case 'long':       return use ? `${mName} ${day}, ${year}` : `${yStr} ${mName} ${toNp(day)} गते`;
    default:           return `${year}-${mStr}-${dStr}`;
  }
}

